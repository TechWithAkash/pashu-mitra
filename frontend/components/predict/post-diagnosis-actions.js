"use client";

import { useLanguage } from "@/context/language-context";
import { VET_HELPLINE } from "@/lib/constants";
import { motion } from "framer-motion";
import { MessageCircle, Phone, FileDown } from "lucide-react";
import { jsPDF } from "jspdf";

export default function PostDiagnosisActions({ result }) {
  const { t } = useLanguage();

  if (!result) return null;

  const isLSD = result.prediction?.toLowerCase().includes("lumpy");
  const confidence = result.confidence;

  // Confidence text
  let confidenceText = "Low";
  if (confidence >= 0.85) confidenceText = "High";
  else if (confidence >= 0.7) confidenceText = "Fair";

  const handleWhatsApp = () => {
    const message = encodeURIComponent(
      `Pashumitra Health Check\n` +
      `Result: ${isLSD ? "Disease Signs Found" : "Healthy"}\n` +
      `Confidence: ${confidenceText}\n` +
      `Advice: ${result.advice || "N/A"}\n` +
      `Check your animals at: ${window.location.origin}`
    );
    window.open(`https://wa.me/?text=${message}`, "_blank");
  };

  const handleDownloadPDF = () => {
    const doc = new jsPDF();
    doc.setFontSize(20);
    doc.text("Pashumitra Health Report", 20, 25);

    doc.setFontSize(11);
    doc.text(`Date: ${new Date().toLocaleDateString()}`, 20, 40);
    doc.text(`Result: ${result.prediction}`, 20, 50);
    doc.text(`Confidence: ${(confidence * 100).toFixed(1)}% (${confidenceText})`, 20, 60);

    if (result.quality_warning) {
      doc.text(`Quality Note: ${result.quality_warning}`, 20, 75);
    }

    if (result.advice) {
      doc.text("Advice:", 20, 90);
      const lines = doc.splitTextToSize(result.advice, 170);
      doc.text(lines, 20, 100);
    }

    doc.setFontSize(9);
    doc.text("Generated by Pashumitra — AI-Powered Cattle Health Check", 20, 280);

    doc.save("pashumitra-report.pdf");
  };

  return (
    <motion.div
      initial={{ opacity: 0, y: 12 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.4, delay: 0.2 }}
      className="space-y-3"
    >
      <p className="text-xs font-semibold tracking-wider uppercase text-muted-foreground">
        {t("result.whatToDoNext")}
      </p>
      <div className="flex flex-wrap gap-3">
        {/* WhatsApp */}
        <button
          onClick={handleWhatsApp}
          className="inline-flex items-center gap-2 px-4 py-2.5 rounded-xl bg-[#25D366]/10 text-[#25D366] border border-[#25D366]/20 text-sm font-medium hover:bg-[#25D366]/20 transition-colors"
        >
          <MessageCircle className="h-4 w-4" />
          {t("action.shareWhatsApp")}
        </button>

        {/* Call Vet */}
        <a
          href={`tel:${VET_HELPLINE}`}
          className="inline-flex items-center gap-2 px-4 py-2.5 rounded-xl bg-destructive/10 text-destructive border border-destructive/20 text-sm font-medium hover:bg-destructive/20 transition-colors"
        >
          <Phone className="h-4 w-4" />
          {t("action.callVet")} ({VET_HELPLINE})
        </a>

        {/* Download PDF */}
        <button
          onClick={handleDownloadPDF}
          className="inline-flex items-center gap-2 px-4 py-2.5 rounded-xl bg-primary/10 text-primary border border-primary/20 text-sm font-medium hover:bg-primary/20 transition-colors"
        >
          <FileDown className="h-4 w-4" />
          {t("action.downloadReport")}
        </button>
      </div>
    </motion.div>
  );
}
